    /// Defines log level type
    /// </summary>
    public enum LogLevelType
    {
        /// <summary>
        /// Debug message
        /// </summary>
        Debug = 10,

        /// <summary>
        /// Information message
        /// </summary>
        Information = 20,

        /// <summary>
        /// Warning message
        /// </summary>
        Warning = 30,

        /// <summary>
        /// Error message
        /// </summary>
        Error = 40,

        /// <summary>
        /// Fatal error message
        /// </summary>
        Fatal = 50
    }
}
